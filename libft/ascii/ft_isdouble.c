#include "../libft.h"

int	ft_isdouble(char *s)
{
	int	i;

	i = 0;
	if (ft_isdigit(s[i]) || s[i] == '+' || s[i] == '-')
	{
		i++;
		while (s[i] && s[i] != '\n' && !ft_isspace(s[i]))
		{
			if (!ft_isdigit(s[i]) && s[i] != '.')
				return (i);
			i++;
		}
	}
	return (0);
}
